function DropdownMenuItem({
  className,
  inset,
  variant = "default",
  ...props
}: React.ComponentProps<typeof DropdownMenuPrimitive.Item> & {
  inset?: boolean
  variant?: "default" | "destructive"
}) {
  return (
    <DropdownMenuPrimitive.Item
      data-slot="dropdown-menu-item"
      data-inset={inset}
      data-variant={variant}
      className={cn(
        // Base styles
        "relative flex cursor-pointer items-center gap-2 rounded-lg px-2.5 py-2 text-sm outline-hidden select-none",
        // Text colors
        "text-zinc-300",
        // Hover/focus - liquid glass highlight
        "focus:bg-white/10 focus:text-white",
        "hover:bg-white/5",
        // Transition
        "transition-colors duration-150",
        // Destructive variant
        "data-[variant=destructive]:text-red-400",
        "data-[variant=destructive]:focus:bg-red-500/10 data-[variant=destructive]:focus:text-red-300",
        "data-[variant=destructive]:*:[svg]:!text-red-400",
        // Icons
        "[&_svg:not([class*='text-'])]:text-zinc-500 [&_svg]:pointer-events-none [&_svg]:shrink-0 [&_svg:not([class*='size-'])]:size-4",
        // Disabled
        "data-[disabled]:pointer-events-none data-[disabled]:opacity-50",
        // Inset
        "data-[inset]:pl-8",
        className
      )}
      {...props}
    />
  )
}

